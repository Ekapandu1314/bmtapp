    protected function validator($request)
    {
        switch (request()->method()) {
            case 'POST':
                return Validator::make($request, [
                    'name' => 'required',
                    'photo' => 'required|image',
                ]);
                break;
            
            case 'PUT':
                return Validator::make($request, [
                    'name' => 'required',
                    'date' => 'required|date',
                ]);
                break;
        }
    }
    
